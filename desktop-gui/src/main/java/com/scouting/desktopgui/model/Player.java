package com.scouting.desktopgui.model;

public class Player {
    private String id;
    private String name;
    private String position;
    private int age;

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getPosition() {
        return position;
    }

    public int getAge() {
        return age;
    }
}
